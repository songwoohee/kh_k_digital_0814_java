//            System.out.printf("%d %d %d", a, b, c);
//        }
        // (2)번 방법
        if(t % 10 != 0) {
            System.out.println("-1");
            return;
        }
        a = t / 300;      // 5분 버튼에 대한 몫 (5분 버튼이 눌려진 수)
        b = t % 300 / 60; // 1분 버튼이 눌려진 수
        c = t % 300 % 60 / 10; // 10초 버튼이 눌려진 수
        System.out.printf("%d %d %d\n",a,b,c);

